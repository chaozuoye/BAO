namespace General
{
    public struct Vec2
    {
        public int X;
        public int Y;

        public Vec2(int x, int y)
        {
            X = x;
            Y = y;
        }
    }
    public struct Vec3
    {
        public float X;
        public float Y;
        public float Z;
        public Vec3(float x,float y,float z)
        {
            X = x;
            Y = y;
            Z = z;
        }
    }


}